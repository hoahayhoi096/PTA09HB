    def __init__(self, ten, tuoi, lophoc):
        self.ten = ten
        self.tuoi = tuoi
        self.lophoc = lophoc

    def doiTen(self, tenMoi):
        self.ten = tenMoi

    def capNhatTuoi(self, tuoiMoi):
        self.tuoi = tuoiMoi

    def doiLopHoc(self, lopMoi):
        self.lophoc = lopMoi


